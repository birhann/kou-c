int main()
{
    // Function returns as void. A function with no return value has the return type as void.
    void exit(int status);

    // Function arguments as void A function with no parameter can accept a void.
    int rand(void);

    // Pointers to void. A pointer of type void * represents the address of an object, but not its type.
    void *malloc(size_t size);

    return 0;
}